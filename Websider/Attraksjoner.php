					<p id ="Haakonshallen_text"> Håkonshallen er 750 år gammel og ble bygget av Kong Håkon Håkonsson som kongebolig og festsal. 
						Da hans sønn Magnus Håkonsson Lagabøte giftet seg med sin danske prinsesse Ingeborg i 1261, var  2000 gjester invitert, og det ble dekket i tre bygninger for å få plass til alle. ”Kongene holdt til i steinhallen”, heter det i sagaberetningen.
						Dengang var Bergen Norges største og viktigste by, og Håkonshallen ble åsted for store rikspolitiske begivenheter, blant annet utformingen av Norges første samlede lovverk. Mellom de tykke steinveggene kan du ennå fornemme gjenklangen av 
						middelalderhoffets høytidelige seremonier og lystige etegilder. Som nasjonalt kulturminne er Håkonshallen fremdeles i bruk til både kongelige middager og andre offisielle sammenhenger. H. M. Kongens faste plass er ikke vanskelig å finne.
						Hallen har tre etasjer, der den øverste er festsalen. Den mellomste etasjen var for administrasjonen, kanselliet (kongens råd). 1. etasje eller kjelleren var sannsynligvis forrådslageret.
						I Håkonshallen bor det mye historie. Her ble den første loven som gjaldt for hele Norge sannsynligvis skrevet, og det var i denne tiden, ved midten av 1200-tallet, 
						at Norge fikk sin første hovedstad: Bergen. Rosenkrantztårnet ble oppført som et kombinert forsvars- og residenstårn i 1560-årene av lensherren Erik Rosenkrantz. 
						To eldre anlegg ble bygget inn i tårnet; kong Magnus Lagabøters kastell, en mindre festning fra ca. 1270 og Jørgen Hanssøns forverk fra ca. 1520. Dette har gitt tårnet en eiendommelig grunnplan og romordning med bl.a. forskyvninger i etasjehøyden. 
						Med sin strategiske plassering i sørfløyen, har tårnet vært en hjørnestein i borgsystemet på Bergenhus festning. </p>
